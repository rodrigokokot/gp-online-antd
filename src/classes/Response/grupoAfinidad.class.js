function GruposAfinidadResponse(grupo) {
    this.IdGrupoAfinidad = grupo.idGrupoAfinidad;
    this.Descripcion = grupo.descripcion;
    this.IdTipoProducto = grupo.idTipoProducto;
    this.ArtePlastico = grupo.artePlastico;
    this.HabilitarEmbozado = grupo.habilitarEmbozado;
    this.IdModeloRestriccion = grupo.idModeloRestriccion;
    this.RowVersion = grupo.rowVersion;
    this.IdEstado = grupo.idEstado;
    this.ModeloRestriccion = grupo.modeloRestriccion;
    this.TipoProducto = grupo.tipoProducto;
}

export default GruposAfinidadResponse